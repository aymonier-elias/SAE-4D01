<section class="concept-section doubleBorder">
    <h3>Notre histoire</h3>
    <p>We-Escape est né en Allemagne. Ce qui a commencé comme un projet local d’escape games en plein air a rapidement séduit un public en quête d’aventures hors des murs. Fort de ce succès, nous nous étendons aujourd’hui à l’étranger pour proposer nos missions au plus grand nombre.</p>
    <p>Notre philosophie reste la même : sortir des salles fermées et utiliser l’espace réel comme terrain de jeu. Que ce soit dans les rues d’une ville, au cœur d’un parc ou sur les sentiers, l’aventure vous attend dehors.</p>
