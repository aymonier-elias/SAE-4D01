<section class="concept-section doubleBorder">
    <h3>L’escape game en extérieur, c’est quoi ?</h3>
    <p>Un escape game, ou jeu d’évasion grandeur nature, est une activité immersive et ludique où les participants doivent résoudre des énigmes et accomplir des missions dans un temps limité. Contrairement aux escape games en salle, notre concept se déroule <strong>entièrement en extérieur</strong>.</p>
    <p>Les participants explorent un lieu réel — ville, parc, forêt, montagne — et suivent une intrigue avec des indices disséminés sur leur parcours. Chaque rue, chaque détail peut être la clé qui vous rapproche de la victoire. Le jeu se pratique en équipe (généralement de 2 à 6 joueurs) et repose sur la coopération, la réflexion et l’esprit d’observation.</p>
    <p>Que vous soyez débutant ou passionné, vous ne serez pas au bout de vos surprises : nos scénarios se déclinent en aventure, enquête, science-fiction, horreur, et bien plus encore.</p>
</section>

<section class="concept-section doubleBorder">
    <h3>Pourquoi dehors ?</h3>
    <p>L’extérieur offre un cadre changeant, des décors naturels et une liberté de mouvement que les salles ne peuvent pas offrir. Vous respirez, vous bougez, vous explorez. La météo et la lumière font partie du jeu. Chaque mission est une vraie sortie, mémorable et partagée.</p>
</section>

